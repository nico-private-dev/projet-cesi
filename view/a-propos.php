        <h1>À propos de Qr FIM</h1>
        <p>Qr FIM est une initiative conçue et développée par les étudiants et pour les étudiants de [Nom de l'École]. Notre mission est de simplifier le partage d'informations au sein de notre communauté éducative en utilisant la technologie des QR codes.</p>
        <br>
        <p>Dans notre quête d'innovation et d'efficacité, nous avons remarqué que les liens vers des ressources éducatives, des événements et des annonces importantes pouvaient parfois être longs et complexes. [Nom de l'Application] résout ce problème en offrant un moyen rapide et facile de créer des QR codes pour des URLs raccourcies, spécialement conçues pour notre école. Ces codes peuvent être scannés en un instant, menant directement aux ressources partagées sans le tracas de taper des adresses web longues et compliquées.</p>
        <br>

